# Using PEMDAS (Parentheses, Exponents, Multiplication and Division (from left to right), Addition and Subtraction (from left to right))
# ()
# **
# * OR / (from left to right)
# + OR - (from left to right)
print("PEMDAS")
print(3 * 3 + 3 / 3 - 3)
print(3 * (3 + 3) / 3 - 3)
print(3 + 5 * (2 - 8) / 4)

# Using Parentheses
print((3 + 5) * 2 - 8 / 4)

# Using Parentheses with Exponentiation
print((3 + 5) * (2 - 8) / 4)

# Using Parentheses with Floor Division
print((3 + 5) * (2 - 8) // 4)

# Using Parentheses with Modulus
print((3 + 5) * (2 - 8) % 4)

# Using Parentheses with Exponentiation and Floor Division
print((3 + 5) * (2 - 8) // 4 ** 2)

# Using Parentheses with Exponentiation and Modulus
print((3 + 5) * (2 - 8) % 4 ** 2)

# Using Parentheses with Exponentiation and Floor Division and Modulus
print((3 + 5) * (2 - 8) // 4 ** 2 % 3)

# Using Parentheses with Exponentiation and Floor Division and Modulus with Addition
print((3 + 5) * (2 - 8) // 4 ** 2 % 3 + 1)
